"""
evaluate_and_shap.py

Evaluates the fine-tuned BEHRT model (T2D prediction) on the full test set
AND on the 4 horizon-censored test sets generated by
1preprocessing/04_generate_horizon_test_sets.py (5y / 3y / 2y / 1y).

For each horizon:
  1. Computes metrics: ROC-AUC, F1, Precision, Recall, Specificity,
     confusion matrix.
  2. Computes SHAP at the clinical-code level (which codes push the
     prediction towards "diabetic"), aggregated over a sample of patients.

Outputs (in OUTPUT_DIR):
  - metrics_by_horizon.csv          <- comparison table across 5y/3y/2y/1y/full
  - confusion_matrix_<horizon>.png
  - shap_top_codes_<horizon>.csv
  - shap_top_codes_<horizon>.png

Requirements:
    pip install shap --break-system-packages   (in addition to what you
    already use: torch, pytorch_pretrained_bert, scikit-learn, matplotlib,
    seaborn, duckdb)

NOTE ON THE SHAP DESIGN HERE:
shap.GradientExplainer is used over the input EMBEDDINGS (not over the
discrete input_ids, because SHAP needs a continuous, differentiable space to
perturb). age_ids, seg_ids and attention_mask are kept FIXED to each
patient's real values (not perturbed) -- only "which active code tokens push
the prediction" is explained, using the embedding of the PAD token as the
baseline (equivalent to "code absent"). This is the standard approach for
SHAP-style interpretability on BERT/EHR models.
"""

